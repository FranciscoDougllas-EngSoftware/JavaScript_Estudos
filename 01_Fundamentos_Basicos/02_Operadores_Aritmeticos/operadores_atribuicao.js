//Operadores em JAvaScript
/*No JavaScript, temos 6 operadores, de atribuicao que são:
    - +=
    - -=
    - *=
    - /=
    - %=
    - **=
*/


let num3 = 10;
num3 += 5; 
console.log(num3);

num3 -= 5; 
console.log(num3);

num3 *= 5; 
console.log(num3);

num3 /= 5; 
console.log(num3);

num3 %= 15; 
console.log(num3);

num3 **= 4; 
console.log(num3);